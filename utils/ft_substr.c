#include "libft.h"

char	*ft_substr(char	const *s, unsigned int start, size_t len)
{
	size_t	i;
	size_t	s_len;
	char	*str;

	if (!s)
		return (NULL);
	else if (!*s && !start)
		return (ft_strdup(""));
	s_len = ft_strlen(s);
	if (start > s_len)
		len = 0;
	else if (s_len - start < len)
		len = s_len - start;
	str = malloc((len + 1) * sizeof(char));
	if (!str)
		return (NULL);
	i = 0;
	while (i < len)
	{
		str[i] = s[start + i];
		i++;
	}
	str[len] = '\0';
	return (str);
}
