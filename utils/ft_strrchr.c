#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	len;
	char	*str;

	len = ft_strlen(s);
	str = (char *)s;
	while (str[len] != (char)c)
	{
		if (!len)
			return (NULL);
		len--;
	}
	return (str + len);
}
